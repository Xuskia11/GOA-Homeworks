# lowercase
name = input("please enter your name here: ")
print(name.lower())

# uppercase
name = input("please enter your name here: ")
print(name.upper())

# capitalize
surname = input("please enter your surname here: ")
print(surname.capitalize())

# find
word = input("please enter random word here: ")
find = input("please enter letter which you want to find: ")
print(word.find(find))

# join
word = input("please enter random word: ")
change = input("please enter here symbol which you want to write in spaces")
print(change.join(word))